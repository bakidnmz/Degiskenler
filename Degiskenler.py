# Matematiksel Operatörler (x=5,y=3)
print(x+y)  # toplama x+y=5+3=8
print(x-y)  # çıkarma x-y=5-3=2
print(x*y)  # çarpma x*y=5*3=15
print(x/y)  # bölme x/y=5/3=1,6667
print(x//y)  # tambölme x//y=5//3=1
print(x % y)  # mod alma x%y=5%3=2 (5'in 3'e bölümünden kalan)
print(x**y)  # üs alma x**y=5**3=125

